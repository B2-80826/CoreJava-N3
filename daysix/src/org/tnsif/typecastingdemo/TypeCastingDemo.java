package org.tnsif.typecastingdemo;
//Program Demonstrate on Type Casting 
public class TypeCastingDemo {

	public static void main(String[] args) {
		
		//Implicit as0a]]}type casting
		
	
		int a=15;
		float b=a;
		System.out.println("The value of A is " +b);
		
		
		//Explicit as0a]]}type casting
		
		double d=15.5;
		long  l= (long)d;
		System.out.println("The value of A is " +l);
		
		
		
	} 

}
